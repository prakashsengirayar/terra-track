class _AppLocalizationsDelegate extends LocalizationsDelegate<AppLocalizations> {
  const _AppLocalizationsDelegate();
  @override bool isSupported(Locale locale) => ['en','ta','hi'].contains(locale.languageCode);
  @override Future<AppLocalizations> load(Locale locale) async {
    switch (locale.languageCode) {
      case 'ta': return _AppLocalizationsTa();
      case 'hi': return _AppLocalizationsHi();
      default:   return _AppLocalizationsEn();
    }
  }
  @override bool shouldReload(_) => false;
}

